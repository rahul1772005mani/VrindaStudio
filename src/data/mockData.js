// Helper function: Get trending stickers
export const getTrendingStickers = () => stickers.filter(s => s.isTrending);

// Helper function: Get new stickers
export const getNewStickers = () => stickers.filter(s => s.isNew);

// Helper function: Get stickers by category
export const getStickersByCategory = (categoryName) => {
  if (categoryName === 'all' || categoryName === 'All') return stickers;
  return stickers.filter(s => s.category.toLowerCase() === categoryName.toLowerCase());
};

// Helper function: Get a sticker by ID
export const getStickerById = (id) => stickers.find(s => s.id === parseInt(id));
